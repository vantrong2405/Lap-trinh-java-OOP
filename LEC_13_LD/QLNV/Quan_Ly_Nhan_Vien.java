    private void btThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemMoiActionPerformed
        // TODO add your handling code here:
        //them moi 
        int dem = 0;
        String ma, ten, ngaySinh, chucVu = null, soDienThoai, soNgayLam, soNgayDiTre, soGioTangCa;
        boolean gioiTinh;
        Nguoi y = null;
        ma = txtID.getText();
        ten = txtTen.getText();
        ngaySinh = txtNgaySinh.getText();
        soDienThoai = txtDienThoai.getText();
//        soNgayLam = Double.parseDouble(txtSoNgayLam.getText());
//        soNgayDiTre = Double.parseDouble(txtSoNgayDiTre.getText());
//        soGioTangCa = Double.parseDouble(txtSoGioTangCa.getText());
        soNgayLam = txtSoNgayLam.getText();
        soGioTangCa = txtSoGioTangCa.getText();
        soNgayDiTre = txtSoNgayDiTre.getText();

        gioiTinh = gioiTinhNam.isSelected(); //1)sai cho boolean

        int index = cbbChucVu.getSelectedIndex();
        if (index >= 0) {
            chucVu = cbbChucVu.getItemAt(index);
        }
        String chucVu1 = null;
        chucVu1 = chucVu.substring(0, 2);//2 ky tu 
        //ma.isEmpty() la nếu (mã rỗng)   và !ma.isEmpty() là nếu mã không rỗng
        if (!ma.isEmpty() && !ten.isEmpty() && !ngaySinh.isEmpty() && !chucVu.isEmpty() && !soDienThoai.isEmpty() && !soNgayLam.isEmpty() && !soNgayDiTre.isEmpty() && !soGioTangCa.isEmpty()) {
            double snl = Double.parseDouble(soNgayLam);
            double sgtc = Double.parseDouble(soGioTangCa);
            double sndt = Double.parseDouble(soNgayDiTre);
            if (ma.length() > 0 && ten.length() > 0 && ngaySinh.length() > 0 && soDienThoai.length() > 0
                    && soGioTangCa.length() > 0 && soNgayDiTre.length() > 0 && soNgayLam.length() > 0) {
                if (chucVu1.trim().equalsIgnoreCase("Th".trim())) {
                    y = new ThucTapSinh(ma, ten, ngaySinh, gioiTinh, chucVu, soDienThoai, snl, sndt, sgtc);
                } else if (chucVu1.trim().equalsIgnoreCase("Nh".trim())) {
                    y = new NhanVien(ma, ten, ngaySinh, gioiTinh, chucVu, soDienThoai, snl, sndt, sgtc);
                } else if (chucVu1.trim().equalsIgnoreCase("Ph".trim())) {
                    y = new PhoPhong(ma, ten, ngaySinh, gioiTinh, chucVu, soDienThoai, snl, sndt, sgtc);
                } else if (chucVu1.trim().equalsIgnoreCase("Tr".trim())) {
                    y = new TruongPhong(ma, ten, ngaySinh, gioiTinh, chucVu, soDienThoai, snl, sndt, sgtc);
                } else if (chucVu1.trim().equalsIgnoreCase("Gi".trim())) {
                    y = new GiamDoc(ma, ten, ngaySinh, gioiTinh, chucVu, soDienThoai, snl, sndt, sgtc);
                }
            }
            if (n == 0) {// lay thằng đầu tiên 
                a.add(y);
                n++;
            }
            for (int i = 0; i < a.size(); i++) { // check thằng đầu với thằng nhập vào  
                if (ma.trim().equalsIgnoreCase(a.get(i).getMa().trim())) {
                    if (n != 1) {
                        JOptionPane.showMessageDialog(rootPane, "Mã bị trùng ! vui lòng nhập lại");
                        break;
                    }
                } else {
                    dem++;
//                    System.out.println("dem : " + dem);
//                    System.out.println("a.size : " + a.size());
                    if (dem == a.size()) {
                        a.add(y);
                        break;
                    }
                }
            }
            n++;
//            a.add(y);
            showResult();
        } else if (ma.isEmpty()) {
//              JOptionPane.showMessageDialog(rootPane, "Mã không được để trống", "Thông báo", HEIGHT);
            JOptionPane.showMessageDialog(null, "Mã không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tên không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (ngaySinh.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ngày sinh không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (chucVu.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chức vụ không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (soDienThoai.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Số điện thoại không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (soNgayLam.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Số ngày làm không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (soNgayDiTre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Số ngày đi trễ không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (soGioTangCa.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Số giờ tăng ca không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Thông tin không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btThemMoiActionPerformed

