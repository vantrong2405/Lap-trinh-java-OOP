    public void showResult() {//lay du lieu ( tra về dữ liệu ) // in ra du lieu tren bang
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        for (Nguoi y : a) {
            model.addRow(new Object[]{
                y.getMa(), y.getTen(), y.getNgaySinh(), (y.isGioiTinh() ? "Nam" : "Nu"), y.getSoDienThoai(), y.getChucVu(), y.getSoNgayLam(),
                y.getSoNgayDiTre(), y.getSoGioTangCa(), y.phuCap(), y.getThanhTien()
            });
        }
    }

